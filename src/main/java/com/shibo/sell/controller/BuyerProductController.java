        public List<List<Integer>> combinationSum2(int[] candidates, int target) {
            dfs(0, target, new ArrayList<>(), candidates);
            return new ArrayList<>(ans);
        }

        private void dfs(int cur, int target, List<Integer> path, int[] nums) {
            if (target == 0) {
                Collections.sort(path);
                ans.add(new ArrayList<>(path));
                return;
            }
            if (cur == nums.length || memo.contains(new Pair<>(cur, target))) return;

            if (nums[cur] <= target) {
                path.add(nums[cur]);
                dfs(cur + 1, target - nums[cur], path, nums);
                path.remove((Integer) nums[cur]);
            }
            dfs(cur + 1, target, path, nums);
            memo.add(new Pair<>(cur, target));
        }
